import { msg } from "@lingui/core/macro";
import { setI18n } from "@lingui/react/server";
import { Lora } from "next/font/google";
import type { PropsWithChildren } from "react";
import { LinguiClientProvider } from "@/components/language/lingui-client-provider";
import { ThemeProvider } from "@/components/theme/theme-provider";
import {
  allMessages,
  getI18nInstance,
  type PageLangParam,
} from "@/lib/i18n/appRouterI18n";
import linguiConfig from "~/lingui.config";
import "@/css/globals.css";
import { SettingsMenu } from "@/components/settings-menu";

const lora = Lora({
  weight: "400",
  style: "normal",
});

export async function generateStaticParams() {
  return linguiConfig.locales.map((lang) => ({ lang }));
}

export async function generateMetadata(props: PageLangParam) {
  const { lang } = await props.params;
  const i18n = getI18nInstance(lang);

  return {
    title: i18n._(msg`effective`),
    description: i18n._(msg`Generated by create next app`),
    icons: {
      icon: [{ url: "/zap.svg", type: "image/svg+xml" }],
      shortcut: ["/zap.svg"],
    },
  };
}

export default async function RootLayout({
  children,
  params,
}: PropsWithChildren<PageLangParam>) {
  const { lang } = await params;

  const i18n = getI18nInstance(lang);

  setI18n(i18n);

  console.log("lang", lang);

  return (
    <html lang={lang as string} suppressHydrationWarning>
      <body className={`${lora.className} antialiased max-h-dvh overflow-auto`}>
        <LinguiClientProvider
          initialLocale={lang}
          initialMessages={allMessages[lang]}
        >
          <ThemeProvider>
            <SettingsMenu currentLang={lang} />
            {children}
          </ThemeProvider>
        </LinguiClientProvider>
      </body>
    </html>
  );
}
